def accessKey():
    global psw
    while True:
        lock.acquire()
        gpio.mode(greenLed, OUTPUT)
        gpio.mode(redLed, OUTPUT)
        if len(psw) >= 4:
            if psw in accessPsw:
                print("Access granted")
                lock(False)
                oledUnlockPinSequence()
                psw = []
                gpio.set(greenLed, HIGH)
                sleep(3000)
            else:
                print("Access denied")
                psw = []
                oledWrongPinSequence()
                gpio.set(redLed, HIGH)
                sleep(3000)
        lock.release()
        sleep(200)
        gpio.set(greenLed, LOW)
        gpio.set(redLed, LOW)

